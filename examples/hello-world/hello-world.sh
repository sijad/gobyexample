# کد را داخل فایل `hello-world.go` قرار دهید
# و برای اجرای برنامه از دستور `go run` استفاده نمایید.
$ go run hello-world.go
سلام دنیا

# مواقعی که نیاز به خروجی باینری برنامه داریم
# می‌توان از دستور `go build` استفاده کرد.
$ go build hello-world.go
$ ls
hello-world	hello-world.go

# و سپس فایل باینری را اجرا نماییم.
$ ./hello-world
سلام دنیا

